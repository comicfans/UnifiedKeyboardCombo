unsigned int KeyMap::nameToCode(const StringType& name){
    int counter=0;
    for(auto p:HEX_MAP_NAME){
        if(name==p){
            return  counter;
        }
        ++counter;
    }

    return -1;
}

StringType KeyMap::codeToName(unsigned int code){
    if(code>255){return StringType(_T("invalid"));}

    return HEX_MAP_NAME[code];
}

